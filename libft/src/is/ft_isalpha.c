#include "../../libft.h"

/**
 * @brief a-z, A-Z
 * @return “true” if it's an alpha 
*/
int	ft_isalpha(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}
