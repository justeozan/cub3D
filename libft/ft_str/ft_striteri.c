#include "../libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	i;

	if (!s || !(*s) || !f)
		return ;
	i = -1;
	while (s[++i])
		(*f)(i, &s[i]);
}
